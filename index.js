
//for loop
const myArray = [];

for(let i = 1; i < 10; i++) {
    myArray.push(i);
}

//while loop

const myArray = [];
let i = 10;

while(i >= 0){
    myArray.push(i);
    i--;
}

console.log(myArray)


//odd numbers
const myArray = [];

for (let i = 11; i < 21; i += 2){
myArray.push(i);
}

console.log(myArray)

//backwards

const myArray = [];
for (let i = 15; i > 0; i -=2){
    myArray.push(i);
}

